public class Arrays_conc {
    public static void main(String args[]){
        int arr[]={1,2,3,4,5};
        String str[]={"java","c","python"};
        for(int i=0;i<arr.length;i++){
            System.out.print(arr[i]+" ");
        }
        System.out.println();
        for(int i=0;i<str.length;i++){
            System.out.print(str[i]+" ");
        }
    }
}
